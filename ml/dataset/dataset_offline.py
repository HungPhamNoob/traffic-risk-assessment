#!/usr/bin/env python3
"""
Offline Feature Engineering for H2O Training - US Accidents

Purpose:
    Read the offline training CSV file, clean and encode features,
    and produce a clean CSV ready for H2O AutoML.

Input:
    data/split/us_train_offline_before_2020.csv
    (generated by the split script)

Output:
    data/process/us_train_offline_before_2020.csv
    (ready to be loaded into H2O for training)
"""

import logging
import os
from datetime import datetime
from pathlib import Path
from typing import Optional, Any

import pandas as pd
from dotenv import load_dotenv

# ============================================================
# Environment and logging
# ============================================================

load_dotenv()

logging.basicConfig(
    level=os.getenv("LOG_LEVEL", "INFO"),
    format="%(asctime)s [%(levelname)s] %(message)s",
)

logger = logging.getLogger("offline-feature-engineering")


# ============================================================
# Configuration (can be overridden via .env)
# ============================================================

INPUT_CSV_PATH = Path(
    os.getenv(
        "OFFLINE_TRAIN_INPUT",
        "data/split/us_train_offline_before_2020.csv",
    )
)

OUTPUT_CSV_PATH = Path(
    os.getenv(
        "OFFLINE_TRAIN_OUTPUT",
        "data/process/us_train_offline_before_2020.csv",
    )
)

# ============================================================
# Helper functions for safe type conversion
# ============================================================


def safe_float(value: Any, default: Optional[float] = None) -> Optional[float]:
    """Convert a value to float, returning default if conversion fails."""
    if pd.isna(value):
        return default
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def safe_int(value: Any, default: Optional[int] = None) -> Optional[int]:
    """Convert a value to int, returning default if conversion fails."""
    if pd.isna(value):
        return default
    try:
        return int(float(value))
    except (TypeError, ValueError):
        return default


def safe_string(value: Any, default: str = "") -> str:
    """Convert a value to a clean string."""
    if pd.isna(value):
        return default
    text = str(value).strip()
    return text if text else default


# ============================================================
# Feature encoding functions
# (logic matches Spark batch_preprocessing.py exactly)
# ============================================================


def encode_weather_condition(weather_text: Any) -> int:
    """
    Map raw weather description to a numeric code.

    Codes:
        0 = clear / normal / unknown
        1 = rain / drizzle / shower
        2 = snow / ice / sleet
        3 = fog / haze / mist
        4 = storm / thunder
        5 = cloudy / overcast
        6 = windy
    """
    # Handle NaN, None, or empty values safely
    weather = safe_string(weather_text).lower().strip()

    if not weather:
        return 0

    if "thunder" in weather or "storm" in weather:
        return 4
    if "snow" in weather or "ice" in weather or "sleet" in weather:
        return 2
    if "rain" in weather or "drizzle" in weather or "shower" in weather:
        return 1
    if "fog" in weather or "haze" in weather or "mist" in weather:
        return 3
    if "cloud" in weather or "overcast" in weather:
        return 5
    if "wind" in weather:
        return 6

    return 0


def encode_road_type(street: Any) -> int:
    """
    Infer road type from street name.

    Codes:
        0 = local / unknown
        1 = interstate / freeway / highway
        2 = route / state route / US route
        3 = street
        4 = avenue
        5 = boulevard
        6 = drive
        7 = road
    """
    street = safe_string(street).lower().strip()

    if not street:
        return 0

    if any(
        token in street
        for token in ["interstate", "i-", "freeway", "fwy", "highway", "hwy"]
    ):
        return 1
    if any(token in street for token in ["route", "state route", "us-", "sr-"]):
        return 2
    if any(token in street for token in ["street", " st", "st."]):
        return 3
    if any(token in street for token in ["avenue", " ave", "ave."]):
        return 4
    if any(token in street for token in ["boulevard", " blvd", "blvd."]):
        return 5
    if any(token in street for token in ["drive", " dr", "dr."]):
        return 6
    if any(token in street for token in ["road", " rd", "rd."]):
        return 7

    return 0


def parse_boolean(value: Any) -> int:
    """Convert a boolean-like CSV field to 0 or 1."""
    text = safe_string(value).lower()
    if text in {"true", "1", "yes", "y"}:
        return 1
    return 0


def parse_datetime(value: Any) -> Optional[datetime]:
    """Parse US Accidents timestamp string into datetime object."""
    text = safe_string(value)
    if not text:
        return None

    # Try common formats
    for fmt in [
        "%Y-%m-%d %H:%M:%S",
        "%Y-%m-%d %H:%M:%S.%f",
        "%Y-%m-%dT%H:%M:%S",
        "%Y-%m-%dT%H:%M:%S.%f",
    ]:
        try:
            return datetime.strptime(text.replace("Z", ""), fmt)
        except ValueError:
            continue

    # Try ISO format as last resort
    try:
        return datetime.fromisoformat(text.replace("Z", "+00:00"))
    except ValueError:
        return None


# ============================================================
# Main feature engineering function
# ============================================================


def prepare_training_features(input_path: Path, output_path: Path) -> pd.DataFrame:
    """
    Load raw CSV, engineer features, and save clean CSV for H2O.

    Parameters
    ----------
    input_path : Path
        Path to the raw training CSV file.
    output_path : Path
        Path where the cleaned featured CSV will be saved.

    Returns
    -------
    pd.DataFrame
        The processed feature DataFrame ready for H2O training.
    """
    logger.info("=" * 80)
    logger.info("Offline Feature Engineering for H2O Training")
    logger.info("=" * 80)
    logger.info("Input CSV:  %s", input_path)
    logger.info("Output CSV: %s", output_path)

    # ---- Step 1: Load raw CSV ----
    logger.info("Step 1: Loading raw CSV...")
    df = pd.read_csv(input_path, low_memory=False)
    n_rows_raw = len(df)
    logger.info("Loaded %s raw rows, %s columns", f"{n_rows_raw:,}", len(df.columns))

    # ---- Step 2: Parse timestamp ----
    logger.info("Step 2: Parsing Start_Time...")
    df["event_time"] = df["Start_Time"].apply(parse_datetime)
    n_missing_time = df["event_time"].isna().sum()
    logger.info(
        "Parsed timestamps; %s rows have invalid/missing Start_Time",
        f"{n_missing_time:,}",
    )
    df = df[df["event_time"].notna()].copy()
    logger.info("Rows remaining after timestamp filter: %s", f"{len(df):,}")

    # Derive time features
    df["event_year"] = df["event_time"].dt.year
    df["hour"] = df["event_time"].dt.hour
    # Use Spark dayofweek convention: 1=Sunday, 7=Saturday
    df["day_of_week"] = df["event_time"].apply(lambda t: (t.isoweekday() % 7) + 1)
    df["is_weekend"] = df["day_of_week"].isin([1, 7]).astype(int)
    df["is_rush_hour"] = (
        ((df["hour"] >= 7) & (df["hour"] <= 9))
        | ((df["hour"] >= 16) & (df["hour"] <= 18))
    ).astype(int)

    # ---- Step 3: Encode weather ----
    logger.info("Step 3: Encoding weather...")
    df["weather_code"] = df["Weather_Condition"].apply(encode_weather_condition)

    # ---- Step 4: Encode road type ----
    logger.info("Step 4: Encoding road type...")
    df["road_type_code"] = df["Street"].apply(encode_road_type)

    # ---- Step 5: Parse boolean features ----
    logger.info("Step 5: Parsing boolean road/context features...")
    boolean_columns = [
        "Amenity",
        "Bump",
        "Crossing",
        "Give_Way",
        "Junction",
        "No_Exit",
        "Railway",
        "Roundabout",
        "Station",
        "Stop",
        "Traffic_Calming",
        "Traffic_Signal",
    ]

    for col_name in boolean_columns:
        df[col_name] = df[col_name].apply(parse_boolean)

    # ---- Step 6: Light condition ----
    logger.info("Step 6: Encoding light condition (day/night)...")
    df["is_night"] = (
        df["Sunrise_Sunset"].astype(str).str.lower().str.strip().eq("night").astype(int)
    )

    # ---- Step 7: Select and rename final columns ----
    logger.info("Step 7: Selecting final feature columns...")

    df_output = pd.DataFrame()

    # Metadata (useful for tracing but NOT used as model features)
    df_output["event_id"] = df["ID"].apply(safe_string)
    df_output["event_year"] = df["event_year"]
    df_output["event_time"] = df["event_time"].dt.strftime("%Y-%m-%dT%H:%M:%S")

    # Label (the column we want to predict - NOT an input feature)
    df_output["true_severity"] = df["Severity"].apply(safe_int)

    # Geospatial features
    df_output["lat"] = df["Start_Lat"].apply(safe_float)
    df_output["lon"] = df["Start_Lng"].apply(safe_float)

    # Time features
    df_output["hour"] = df["hour"]
    df_output["day_of_week"] = df["day_of_week"]
    df_output["is_weekend"] = df["is_weekend"]
    df_output["is_rush_hour"] = df["is_rush_hour"]

    # Weather features
    df_output["weather_code"] = df["weather_code"]
    df_output["temperature_f"] = df["Temperature(F)"].apply(safe_float, default=50.0)
    df_output["humidity"] = df["Humidity(%)"].apply(safe_float, default=50.0)
    df_output["wind_speed_mph"] = df["Wind_Speed(mph)"].apply(safe_float, default=0.0)
    df_output["visibility_mi"] = df["Visibility(mi)"].apply(safe_float, default=10.0)

    # Road / context features
    df_output["road_type_code"] = df["road_type_code"]
    df_output["is_junction"] = df["Junction"].apply(safe_int)
    df_output["has_traffic_signal"] = df["Traffic_Signal"].apply(safe_int)
    df_output["is_crossing"] = df["Crossing"].apply(safe_int)
    df_output["is_roundabout"] = df["Roundabout"].apply(safe_int)
    df_output["is_stop"] = df["Stop"].apply(safe_int)
    df_output["is_station"] = df["Station"].apply(safe_int)
    df_output["is_railway"] = df["Railway"].apply(safe_int)

    # Light feature
    df_output["is_night"] = df["is_night"]

    # ---- Step 8: Drop invalid rows ----
    logger.info("Step 8: Dropping rows with missing critical values...")
    critical_columns = [
        "event_id",
        "true_severity",
        "lat",
        "lon",
    ]
    n_before_drop = len(df_output)
    df_output = df_output.dropna(subset=critical_columns)
    n_after_drop = len(df_output)
    logger.info(
        "Dropped %s rows with missing critical fields; %s rows remaining",
        f"{n_before_drop - n_after_drop:,}",
        f"{n_after_drop:,}",
    )

    # ---- Step 9: Save to CSV ----
    logger.info("Step 9: Saving clean features to CSV...")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df_output.to_csv(output_path, index=False)

    file_size_mb = output_path.stat().st_size / (1024 * 1024)
    logger.info(
        "Saved %s rows to %s (%.2f MB)", f"{n_after_drop:,}", output_path, file_size_mb
    )
    logger.info("=" * 80)
    logger.info("Offline feature engineering completed successfully.")
    logger.info("=" * 80)

    return df_output


# ============================================================
# Main entry point
# ============================================================


def main() -> None:
    """Run the full offline feature engineering pipeline."""
    if not INPUT_CSV_PATH.exists():
        logger.error(
            "Input file not found: %s. "
            "Run the data split script first to generate training CSV.",
            INPUT_CSV_PATH,
        )
        raise SystemExit(1)

    prepare_training_features(INPUT_CSV_PATH, OUTPUT_CSV_PATH)


if __name__ == "__main__":
    main()
